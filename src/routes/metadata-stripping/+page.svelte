<section class="prose prose-primary my-4">
  <h1>Metadata Stripping</h1>
  <p>
    In order to put your photos in albums properly, Traist Photos reads special data embedded in the
    photos, known as 'EXIF tags', a form of metadata.
  </p>
  <p>
    This data often includes information like the device which took the photo, the time the photo
    was taken, and often with mobile phones, the GPS location of the photo.
  </p>
  <p>
    As this data could be used in ways that you might not intend, some devices choose to remove this
    data before sending your files to any cloud services (for example, iPhones do this if you use
    the high efficiency file type).
  </p>
  <p>
    If you're reading this, you were warned that your device is doing this, and the file metadata is
    being stripped.
  </p>
  <h2>What does this mean?</h2>
  <p>
    What this means is that in order to determine when your photo was taken, we need to rely on
    another piece of metadata which is not stored in the tags which have been removed, known as the
    Last Modified Date. It's important to note that the Last Modified Date is not always the same as
    the date the photos was taken. For example, if you cropped the photo later, then your photo will
    be placed in the album which corresponds to the date of the crop, not the date the photo was
    taken.
  </p>
</section>
